    <div class="lg:col-span-2 space-y-8">
      <h2 class="text-3xl font-bold text-green-700 mb-4">2.1.1 Sejarah Desa</h2>
      <p>
        Pada Tahun 1989 Desa Batupute dibentuk merupakan pemekaran dari Desa Siddo pada saat itu wilayahnya sudah sangat luas,
        sehingga pelayanan pemerintah kepada masyarakat kurang maksimal. Pemerintahan Desa Batupute pertama kali dipimpin oleh
        <strong>Drs. Rukman Hamid</strong> sebagai Kepala Desa Persiapan Batupute.
      </p>
      <p>
        Pada Pilkades pertama periode 1991–1996 dan Pilkades kedua 1997–2001, Drs. Rukman Hamid berhasil terpilih kembali secara berturut-turut.
        Periode 2002–2007, Pilkades ketiga dimenangkan oleh <strong>Ramli Manessa</strong> yang kemudian menjabat kembali pada periode 2008–2014.
      </p>
      <p>
        Pada tahun 2014, setelah Ramli Manessa meninggal dunia, jabatan Pelaksana Tugas Kepala Desa diemban oleh
        <strong>Zainal Abbas, S.Pd</strong> hingga Pilkades kelima. Periode 2017–2023, <strong>Sudarmin A.</strong> terpilih sebagai Kepala Desa.
      </p>
      <p>
        Pada tahun 2022 dilaksanakan Pilkades keenam, dan terpilih <strong>Jaharuddin</strong> sebagai Kepala Desa Batupute periode 2023–2029.
      </p>

      <h2 class="text-3xl font-bold text-green-700 mt-12 mb-4">2.1.2 Wilayah Administratif</h2>
      <p>
        Desa Batupute terletak di Kecamatan Soppeng Riaja, Kabupaten Barru dengan luas wilayah sekitar <strong>6.800 Ha</strong>.
        Adapun batas wilayahnya sebagai berikut:
      </p>
      <ul class="list-disc list-inside mb-6 space-y-1">
        <li>Utara: Desa Cilellang</li>
        <li>Selatan: Desa Siddo</li>
        <li>Timur: Desa Manuba</li>
        <li>Barat: Selat Makassar</li>
      </ul>
      <p>
        Secara geografis, Desa Batupute berada pada koordinat <strong>-4,20775° LS / 119,62043° BT</strong>,
        berjarak sekitar <strong>29 KM</strong> dari Kota Kabupaten Barru dan dapat ditempuh sekitar 30 menit menggunakan kendaraan roda dua atau empat.
      </p>
      <p>
        Desa Batupute terdiri dari 4 dusun dan 12 RT, yaitu:
      </p>
      <ul class="list-disc list-inside space-y-1">
        <li><strong>Dusun Batupute</strong>: RT 01, 02, 03, 04</li>
        <li><strong>Dusun Awarange</strong>: RT 01, 02, 03, 04</li>
        <li><strong>Dusun Ujunge</strong>: RT 01, 02</li>
        <li><strong>Dusun Baturebange</strong>: RT 01, 02</li>
      </ul>
